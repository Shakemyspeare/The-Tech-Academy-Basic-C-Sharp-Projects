using System;
using System.Collections.Generic;
using System.Reflection.Metadata.Ecma335;
using System.Text;

namespace Step_216
{
    public class Math
    {
        //method that adds a predifined number to the user input.
        public static int addTwoNumbers(int num1)
        {
            int result = 15 + num1;
            return result;
        }

        //method that subtracts a predefined number from the user input.
        public static int subtractTwoNumbers(int num1)
        {
            int result = 15 - num1;
            return result;
        }

        //method that multiplies a predefined number to the user input.
        public static int multiplyTwoNumber(int num1)
        {
            int result = 15 * num1;
            return result;
        }
            
            
    }
}
